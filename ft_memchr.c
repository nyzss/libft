#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*loc;

	i = 0;
	loc = (char *)s;
	while (i < n)
	{
		if (loc[i] == c)
			return ((void *)&loc[i]);
		i++;
	}
	return (NULL);
}
