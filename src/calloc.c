#include "libft.h"

void	*ft_calloc(size_t n, size_t size)
{
	void	*allocated;

	allocated = (void *)malloc(n * size);
	if (!allocated)
		return (NULL);
	ft_bzero(allocated, (n * size));
	return (allocated);
}
