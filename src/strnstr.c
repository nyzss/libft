#include "libft.h"

char *strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t i;
	size_t j;

	i = 0;
	if (!needle)
		return ((char *)haystack);
	while (i < len && haystack[i])
	{
		j = 0;
		if (needle[i] == haystack[i])
		{
			while (needle[j] && needle[j] == haystack[j])
			{
				j++;
			}
			if (needle[j] == '\0')
				return ((char *)&haystack[i]);
		}
		i++;
	}
	return (NULL);
}
