#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int		i;
	char	*occ;

	i = 0;
	occ = NULL;
	while (str[i])
	{
		if (str[i] == c)
			occ = (char *)&str[i];
		i++;
	}
	if (str[i] == c)
		occ = (char *)&str[i];
	return (occ);
}
